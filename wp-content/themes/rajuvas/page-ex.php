 <script>

   $count = 0;
  function count($type) {
    if($type == 'plus'){
      $count += 1;      
    }else if($type == 'minus'){
      $count -= 1;
    }
    return $count;
  }

$(function() {
  $(".increase").click(function() {
    if($count<3){
            $("div, div *").children().each(function() {
          var size = parseInt($(this).css("font-size"));
          size = size + 1 + "px";
          $(this).css({
            'font-size': size
          });
        });
        count('plus');     
    }        
  });
  return false;
});
$(function() {
  $(".decrease").click(function() {
    if($count>-3){
    $("div, div *").children().each(function() {
      var size = parseInt($(this).css("font-size"));
      size = size - 1 + "px";
      $(this).css({
        'font-size': size
      });
    });
    count('minus');
  }
  });
  return false;
});

</script>

